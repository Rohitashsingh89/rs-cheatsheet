import mongoose from "mongoose";

const CheatSheetSchema = new mongoose.Schema({
  title: {
    type: String,
    required: true,
  },
  content: {
    type: String,
    required: true,
  },
  created_at: {
    type: Date,
    required: true,
  },
  updated_at: {
    type: Date,
    required: true,
  },
});

export default mongoose.models.CheatSheet ||
  mongoose.model("CheatSheet", CheatSheetSchema);
